/* Operator Logika */

// And && Operator
// logikanya, dia akan mengembalikan nilai true jika semua dari operannya bernilai true;

let a = true
let b = true
result = a && b
console.log(result) // jawabannya: karena a dan b bernilai true, jika salah satu false, maka jawabannya false


// Or Operator ||

let c = true
let d = false 
let orResult = c || d
console.log(orResult) //outputnya true karena salah satu bernilai true

// not Operator !
//kebalikan ya

let x = true
let notResult = !x
console.log(notResult)// outputnya false


