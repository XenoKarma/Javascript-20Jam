/* Operator Aritmatika */

// Penjumlahan
let x = 5
let y = 10
let sum = x + y //output = 15
// console.log(sum, typeof sum)

// Pengurangan
let a = 10
let b = 5
let diff = a - b //output = 10 - 5 = 5
// console.log(diff, typeof diff)

// Operasi Perkalian
let p = 5
let q = 6
let result = p * q //output 30
// console.log(result, typeof result)

// Operasi Pembagian
let m = 100
let n = 5
let bagi = m / n //output 20
// console.log(bagi, typeof bagi) 

// Operai Modulus atau sisa bagi
let r = 15
let s = 4
let mod = r % s //sisa 3 jadi output nya 3
// console.log(mod, typeof mod)

// Operasi Eksponen **
let base = 2
let exponent = 3
let power = base ** exponent //outputnya 8 karena 2 pangkat 3 = 8
// console.log(power, typeof power)


